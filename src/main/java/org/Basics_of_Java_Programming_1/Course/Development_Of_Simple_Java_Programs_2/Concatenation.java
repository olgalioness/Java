package org.Basics_of_Java_Programming_1.Course.Development_Of_Simple_Java_Programs_2;
/*Присваивание переменных*/
public class Concatenation {
    public static void main(String[] args) {
        double i, j, k;

        i = 50.0;
        k = i + 50.0;
        j = k + 1;

        System.out.println("j равно " + j + " и " +
                " k равно " + k);

    }
}