package Course2_DevelopmentOfSimpleJavaPrograms;

public class Concatenation {
    public static void main(String[] args) {
        double i, j, k;

        i = 50.0;
        k = i + 50.0;
        j = k + 1;

        System.out.println("j равно " + j + " и " +
                " k равно " + k);
    }
}